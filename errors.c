#include "push_swap.h"

int	error_syntax(char *str)
{
	if (!(*str == '+' || *str == '-' || (*str >= '0' && *str <= '9')))
		return (1);
	if ((*str == '+' || *str == '-') && !(str[1] >= '0' && str[1] <= '9'))
		return (1);
	while (*++str)
	{
		if (!(*str >= '0' && *str <= '9'))
			return (1);
	}
	return (0);
}

int	error_dup(t_list *a, int n)
{
	if (!a)
		return (0);
	while (a)
	{
		if (a->n == n)
			return (1);
		a = a->next;
	}
	return (0);
}

void	free_stack(t_list **stack)
{
	t_list	*tmp;
	t_list	*current;

	if (!stack)
		return ;
	current = *stack;
	while (current)
	{
		tmp = current->next;
		current->n = 0;
		free(current);
		current = tmp;
	}
	*stack = NULL;
}

void	free_errors(t_list **a)
{
	free_stack(a);
	write(2, "Error\n", 6);
	exit(1);
}
